package mellow.ai.situationHandlers.objects;

public class CardAndValue {

	
	private String card;
	private double value;
	

	public CardAndValue(String card, double value) {
		super();
		this.card = card;
		this.value = value;
	}
	public String getCard() {
		return card;
	}
	public double getValue() {
		return value;
	}
}
